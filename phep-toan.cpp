#include <iostream>

using namespace std;
// Phép toán: + - * / %: phép chia dư,
int main()
{
    int a = 10;
    int b = 3;
    cout << a << " + " << b << " = " << a + b << endl;
    cout << a << " - " << b << " = " << a - b << endl;
    cout << a << " * " << b << " = " << a * b << endl;
    cout << a << " / " << b << " = " << a / b << endl;
    cout << a << " % " << b << " = " << a % b << endl;

    return 0;
}
