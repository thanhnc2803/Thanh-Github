#include <iostream>

using namespace std;
// Phép so sánh: > >= < <= == !=
// 0: Fasle
// 1: True
int main()
{
    int a = 10;
    int b = 5;
    cout << "a > b: " << (a > b) << endl;
    cout << "a >= b: " << (a >= b) << endl;
    cout << "a < b: " << (a < b) << endl;
    cout << "a <= b: " << (a <= b) << endl;
    cout << "a == b: " << (a == b) << endl;
    cout << "a != b: " << (a != b) << endl;

    return 0;
}
